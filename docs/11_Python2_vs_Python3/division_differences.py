# Различия в делении между Python 2 и 3

# Python 2:
# print 5 / 2     # 2 (целочисленное деление)
# print 5.0 / 2   # 2.5 (деление с плавающей точкой)

# Python 3:
print(5 / 2)      # 2.5 (всегда деление с плавающей точкой)
print(5 // 2)     # 2 (целочисленное деление)

# Демонстрация:
a, b = 5, 2
print(f"{a} / {b} = {a / b}")
print(f"{a} // {b} = {a // b}")
print(f"{a} % {b} = {a % b}")

# Для совместимости с Python 2:
from __future__ import division  # в Python 2 это изменило бы поведение 