    salary = 75000.5
    
    # –°—Ç–∞—Ä—ã–π —Å–ø–æ—Å–æ–± (% —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ)
    old_format = "–ú–µ–Ω—è –∑–æ–≤—É—Ç %s, –º–Ω–µ %d –ª–µ—Ç, –∑–∞—Ä–ø–ª–∞—Ç–∞ %.2f —Ä—É–±." % (name, age, salary)
    print(f"–°—Ç–∞—Ä—ã–π —Å–ø–æ—Å–æ–±: {old_format}")
    
    # –ú–µ—Ç–æ–¥ .format()
    format_method = "–ú–µ–Ω—è –∑–æ–≤—É—Ç {}, –º–Ω–µ {} –ª–µ—Ç, –∑–∞—Ä–ø–ª–∞—Ç–∞ {:.2f} —Ä—É–±.".format(name, age, salary)
    print(f"–ú–µ—Ç–æ–¥ .format(): {format_method}")
    
    # –ú–µ—Ç–æ–¥ .format() —Å –∏–º–µ–Ω–æ–≤–∞–Ω–Ω—ã–º–∏ –ø–∞—Ä–∞–º–µ—Ç—Ä–∞–º–∏
