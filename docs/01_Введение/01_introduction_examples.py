    print(f"–°–ª–æ–∂–µ–Ω–∏–µ: {a} + {b} = {a + b}")
    print(f"–í—ã—á–∏—Ç–∞–Ω–∏–µ: {a} - {b} = {a - b}")
    print(f"–£–º–Ω–æ–∂–µ–Ω–∏–µ: {a} * {b} = {a * b}")
    print(f"–î–µ–ª–µ–Ω–∏–µ: {a} / {b} = {a / b}")
    print(f"–¶–µ–ª–æ—á–∏—Å–ª–µ–Ω–Ω–æ–µ –¥–µ–ª–µ–Ω–∏–µ: {a} // {b} = {a // b}")
    print(f"–û—Å—Ç–∞—Ç–æ–∫ –æ—Ç –¥–µ–ª–µ–Ω–∏—è: {a} % {b} = {a % b}")
    print(f"–í–æ–∑–≤–µ–¥–µ–Ω–∏–µ –≤ —Å—Ç–µ–ø–µ–Ω—å: {a} ** {b} = {a ** b}")
    
