    print("=== –ü—Ä–∏–º–µ—Ä 5: –§–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ —Å—Ç—Ä–æ–∫ ===")
    
    name = "–ú–∞—Ä–∏—è"
